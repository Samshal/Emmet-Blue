CREATE SCHEMA Records;
GO
CREATE TABLE Records.PatientData(
PatientId INT(10) PRIMARY KEY IDENTITY,
firstName VARCHAR(60) NOT NULL,
LastName VARCHAR(60) NOT NULL,
Gender VARCHAR(60) NOT NULL,
Age VARCHAR(10) NOT NULL,
Occupation VARCHAR (60) NOT NULL,
Addres VARCHAR (100) NOT NULL,
)
GO
CREATE TABLE Records.LabData(
LabId INT(10) PRIMARY KEY IDENTITY,
LabType VARCHAR (130) NOT NULL,
PatientId INT(10) NOT NULL,
labDate DATETIME,
LabScientistfirstName VARCHAR ((60) NOT NULL,
LabScientistLastName VARCHAR ((60) NOT NULL,
labResult VARCHAR(240) NOT NULL,
FOREIGN KEY (LabId) REFERENCES Records.PatientData(PatientId) ON UPDATE CASCADE ON DELETE CASCADE
)
